
package soutnoce_vm;
/**
 * @author abderrahim
 */
public class vmInPm {
    String Vm;
    String Old_pm;
    String New_pm;

    public vmInPm(String Vm, String Old_pm, String New_pm) {
        this.Vm = Vm;
        this.Old_pm = Old_pm;
        this.New_pm = New_pm;
    }

    public String getVm() {
        return Vm;
    }

    public void setVm(String Vm) {
        this.Vm = Vm;
    }

    public String getOld_pm() {
        return Old_pm;
    }

    public void setOld_pm(String Old_pm) {
        this.Old_pm = Old_pm;
    }

    public String getNew_pm() {
        return New_pm;
    }

    public void setNew_pm(String New_pm) {
        this.New_pm = New_pm;
    }
    
    
}
